package net.jplugin.core.das.route.impl.conn;

import java.sql.Connection;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;

import net.jplugin.core.das.route.impl.conn.NotPreparedStatementUtil.Result;

public class RouterStatement extends EmptyStatement {
	RouterConnection connection;
	ExecuteResult executeResult = new ExecuteResult();
	String sql;

	public static Statement create(RouterConnection conn) {
		RouterStatement cs = new RouterStatement();
		cs.connection = conn;
		return cs;
	}

	@Override
	public ResultSet executeQuery(String sql) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		ResultSet rs = stmt.executeQuery(sql);
		executeResult.fetch(stmt);
		return rs;
	}

	@Override
	public int executeUpdate(String sql) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		int cnt = stmt.executeUpdate(sql);
		executeResult.fetch(stmt);
		return cnt;
	}

	@Override
	public boolean execute(String sql) throws SQLException {
		executeResult.clear();
		Result rr = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = rr.statement;
		sql = rr.resultSql;
		boolean r = stmt.execute(sql);
		executeResult.fetch(stmt);
		return r;
	}

	@Override
	public ResultSet getResultSet() throws SQLException {
		return executeResult.rs;
	}

	@Override
	public int getUpdateCount() throws SQLException {
		return executeResult.updCount;
	}

	@Override
	public Connection getConnection() throws SQLException {
		return connection;
	}

	@Override
	public int executeUpdate(String sql, int autoGeneratedKeys) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		int cnt = stmt.executeUpdate(sql, autoGeneratedKeys);
		executeResult.fetch(stmt);
		return cnt;
	}

	@Override
	public int executeUpdate(String sql, int[] columnIndexes) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		int cnt = stmt.executeUpdate(sql, columnIndexes);
		executeResult.fetch(stmt);
		return cnt;
	}

	@Override
	public int executeUpdate(String sql, String[] columnNames) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		int cnt = stmt.executeUpdate(sql, columnNames);
		executeResult.fetch(stmt);
		return cnt;
	}

	@Override
	public boolean execute(String sql, int autoGeneratedKeys) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		boolean b = stmt.execute(sql, autoGeneratedKeys);
		executeResult.fetch(stmt);
		return b;
	}

	@Override
	public boolean execute(String sql, int[] columnIndexes) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		boolean b = stmt.execute(sql, columnIndexes);
		executeResult.fetch(stmt);
		return b;
	}

	@Override
	public boolean execute(String sql, String[] columnNames) throws SQLException {
		executeResult.clear();
		Result r = NotPreparedStatementUtil.genTargetNotPreparedStatement(connection, sql);
		Statement stmt = r.statement;
		sql = r.resultSql;
		boolean b = stmt.execute(sql, columnNames);
		executeResult.fetch(stmt);
		return b;
	}

	@Override
	public boolean isClosed() throws SQLException {
		return false;
	}

	@Override
	public void close() throws SQLException {
	}

}
